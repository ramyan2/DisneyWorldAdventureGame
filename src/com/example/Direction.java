package com.example;

public class Direction {
    private String directionName;
    private String room;


    /**
     * gets name of the direction
     * @return directionName
     */
    public String getDirectionName() {
        return directionName;
    }

    /**
     * gets room the direction points to
     * @return room
     */
    public String getRoom() {
        return room;
    }
}
